public class Calculator {

      public int Sum(int num1,int num2){
            return num1 + num2;

      }
      public int extraction(int num1,int num2){
            return num1 - num2;

      }
      public int Multi(int num1, int num2){
            return num1 * num2;

      }
      public int Devide(int num1,int num2){
            return num1 / num2;

      }
}
